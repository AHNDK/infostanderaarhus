    <h5>Reglement for anvendelse af Havneskærmen på Havnepladsen</h5>

    <p>For indholdet på Havneskærmen gælder følgende overordnede regler:</p>
    <ul>
      <li>De af politiet opsatte retningslinjer skal overholdes.</li>
      <li>Der må ikke vises stødende billeder, tekst eller spots/film på skærmen.</li>
      <li>
        Skærmen må ikke gøres til brug for reklamering af kommerciel art, og således kun bruges til formidling af aktiviteter, der understøtter anvendelse af pladsen (jf. bl.a. lokalplan 876 § 9 stk. 1).
        <br/>Dog må der:
        <ul>
          <li>Reklameres for et arrangement i byen, også hvor der skal betales entré.</li>
          <li>Henvises til billetsalg vedrørende et arrangement via en hjemmeside eller henvisning til et billetsalgssted.</li>
          <li>Bruges virksomhedsnavne, hvis det indgår i eventtitel, som f.eks. Volvo Ocean Race.</li>
          <li>Figurere logoer fra sponsorer med sekundær placering.</li>
        </ul>
      </li>
      <li>Brug af skærmen knytter sig til eventen og ikke til lejeperioden af Havnepladsen.</li>
      <li>Aktører som lejer pladsen og som ønsker at benytte skærmen, betaler leje svarende til markedsprisen. For arrangementer som Aarhus Kommune har hjemmel til at støtte, fastsættes lejen til et beløb svarende til den faktiske økonomiske omkostning (afskrivning, forbrug og medgået tid).</li>
      <li>Skærmen vil som udgangspunkt være slukket i perioden kl. 23.00 til kl. 7.00, hvorfor der ved aktiviteter uden for dette tidsrum skal søges om lejlighedstilladelse.</li>
      <li>Skærmen må bruges til formidling af aktiviteter som debatmøder, oplæsning af litteratur, digital ’Speakers Corner’, politiske debatter, foredrag, digitale afstemninger/polls, digitalt mødested, ’Silent Concerts’, digitale cykelture, høringer om f.eks. byudvikling, sportsaktiviteter samt understøttelse af andre aktiviteter på pladsen og i området som f.eks. sportsevents, food events, udstillinger og markeder.</li>
    </ul>

    <h5>Krav til billedefiler:</h5>
    <ul>
      <li>Filformat: JPEG</li>
      <li>Opløsning: 900 x 540 pixels (16/9-format)</li>
      <li>Farver: RGB</li>
    </ul>

    <h5>Krav til videomateriale:</h5>
    <ul>
      <li>Opløsning på skærmen er 900x540 pixels.</li>
      <li>Skærmen understøtter video opløsning i 720p (1280x720) og 1080p (1920x1080).</li>
    </ul>

    <h5>Priser</h5>
    <p>Prisen opdeles i to kategorier, jf. reglement:</p>
    <p>For lejere af Havnepladsen, som Aarhus Kommune ikke har hjemmel til at støtte er prisen for leje af Havneskærm pr. døgn: 13.000kr. (eks. moms)</p>
    <p>For lejere af Havnepladsen, som Aarhus Kommune via Kommunalfuldmagten har hjemmel til at støtte, er prisen den faktiske økonomiske omkostning. Kontakt os for at høre nærmere.</p>

