    <h3 id="info" class="info--heading">Information</h3>
    <p>Infostanderne i Aarhus kan benyttes til markedsføring af kulturelle og sportslige events, hvor alle byens borgere kan deltage. De digitale infostandere er placeret ved otte af de mest trafikerede indfaldsveje til Aarhus (Grenåvej, Randersvej, Viborgvej, Edwin Rahrs Vej, Silkeborgvej, Skanderborgvej, Christian X’s Vej og Oddervej).</p>
    <p>Infostanderne er synlige for både fodgængere og cyklister, og de er tændt døgnet rundt. Skærmene er 2 meter brede og 3 meter høje, og de lysende LED-skærme sender klare og tydelige budskaber til de mere end 200.000 trafikanter, der kører forbi hver dag.</p>
    <p>De digitale infostandere viser som udgangspunkt rullende 5 spots ad gangen, og de 8 infostandere viser den samme spotrække.</p>
    <p>Fra foråret 2018 er det muligt at få vist en annonce på Havneskærmen, der står i den nordlige del af Havnepladsen mellem Dokk1 og Navitas. Muligheden gælder for de annoncører, der kan benytte de digitale infostandere ved indfaldsvejene. Man vil kunne købe en uges visning på Havneskærmen, og dermed få vist sin annonce for de mange forbipasserende i den centrale del af byen.</p>
    <p>Visningen på Havneskærmen vil dog afhænge af de store events, der i løbet af året finder sted her, og man kan derfor kun booke visning på Havneskærm, såfremt den ikke benyttes af lejere af Havnepladsen.</p>
    <p>Skærmen er 21,6 kvadratmeter, og den er tændt fra kl. 7.00 til kl. 23.00. Billedeslides ligger på skærmen i et rul med andre billedefiler og videomateriale, dels fra andre annoncører dels fra City Lab Aarhus, læs mere: <a href="http://www.smartaarhus.eu/">http://www.smartaarhus.eu/</a></p>
    <p>Kontakt os for at høre nærmere omkring booking og pris.</p>
    <h5>Fordelen ved digitale spots</h5>
    <ul>
      <li>Eksponering for mere end 200.000 trafikanter hver dag</li>
      <li>Visning på 8 strategisk placerede infostandere</li>
      <li>Visning døgnet rundt</li>
      <li>Visning på lysende LED-skærme (2 x 3 meter)</li>
    </ul>
    <h5>Hvem kan annoncere via Infostander Aarhus?</h5>
    <p>Servicen kan benyttes af alle, der arrangerer kulturelle og sportslige events, hvor alle byens borgere kan deltage, uddannelsesinstitutioner i Aarhus samt kommunale kampagner.</p>
    <h5>Retningslinjer for annoncering</h5>
    <ul>
      <li>Events skal understøtte byens brand</li>
      <li>Der kan kun annonceres for events, der afholdes i Aarhus kommune og som Aarhus Kommune har hjemmel til at støtte</li>
      <li>Uddannelsesinstitutioner i Aarhus Kommune kan annoncere på standerne</li>
      <li>De af politiet opsatte retningslinjer skal overholdes</li>
      <li>Spots må ikke indeholde stødende billeder eller tekst</li>
      <li>
        Der må ikke være reklamer eller kommercielle budskaber på spots, men:
        <ul>
          <li>Der må reklameres for et arrangement i byen, hvor der skal betales entré</li>
          <li>Der må henvises til billetsalg vedrørende et arrangement via en hjemmeside eller henvisning til et billetsalgssted</li>
          <li>Der må bruges virksomhedsnavne, hvis det indgår i eventtitel, som f.eks. GF World Cup</li>
          <li>Logoer fra sponsorer må figurere på spots med sekundær placering</li>
        </ul>
      </li>
    </ul>
    <h5>Praktiske forhold</h5>
    <p>Såfremt vi skal sætte grafikken op, skal følgende materiale leveres pr. mail senest 4 uger før spottet skal vises:</p>
    <ul>
      <li>Baggrundsbillede skal du levere i den bedst mulige opløsning (jpg-fil). Beskriv hvilken del af billedet der skal bruges.</li>
      <li>Teksten bør være korte og præcise budskaber. Du skal også her specificere, hvor teksten skal stå, om den skal stå med store eller små bogstaver, og hvordan den skal sættes op; i linier, i en tekstboble, top, bund osv.</li>
      <li>Det væsentlige er: hvad, hvor og hvornår!</li>
      <li>Et eventuelt logo skal du sende som pdf-, eps- eller ai-fil</li>
    </ul>
    <p>Det er også muligt selv at levere det billede, der skal vises.</p>
