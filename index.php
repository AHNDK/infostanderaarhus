    <h3 id="havneskaerm" class="info--heading">Havneskærm for lejere af Havnepladsen</h3>
    <p>Som lejer af Havnepladsen i det centrale Aarhus kan man leje Havneskærmen, der er placeret i den nordlige ende af pladsen.</p>
    <p>Skærmen er 21,6 kvadratmeter og den er tændt fra kl. 7.00 til kl. 23.00. Afvikles der aktiviteter udenfor dette tidsrum kan der søges lejlighedstilladelse. Skærmen er en LED skærm og har automatisk regulering af lysstyrke baseret på luxmåling i omgivelserne.</p>
    <p>Man kan leje skærmen som en ekstern skærm, eller man kan sende de færdigproducerede billeder og video til os, og så lægger vi det på skærmen i en aftalt periode.</p>

    <br/><br/>

    <p>Der er følgende muligheder:</p>

    <h5>Løsning 1 – visning via Aarhus Kommunes storskærmskanal</h5>
    <p>Man kan få vist sine billeder og video via Aarhus Kommunes storskærmskanal. Materialet sendes til os inkl. beskrivelse af rækkefølge.</p>
    <p>Materialet skal leveres i:</p>
    <p>Video kan leveres i 720p eller 1080p, en mindre opløsning kan også fungere.</p>
    <p>Billeder ses bedst i 16:9 ration/ 1920x1080px</p>

    <br/><br/>

    <h5>Løsning 2 – visning via tilkobling af eget udstyr, uden lyd</h5>
    <p>Tilkobling af egen computer er muligt via vores AV-RACK.</p>
    <p>Video input: et HDMI-kabel er tilstede. Hvis den medbragte computer ikke understøtter HDMI, skal man selv medbringe en konverter.</p>
    <p>Opløsning: op til 4K. Output signal fra den medbragte computer bliver scaleret ned til 900x540 pixels via vores AV-RACK.</p>

    <br/><br/>

    <h5>Løsning 3 – tilkobling af eget udstyr, med lyd.</h5>
    <p>Tilkobling af egen computer er muligt via vores AV-RACK.</p>
    <p>Video input: HDMI-kabel er tilstede. Hvis den medbragte computer ikke understøtter HDMI, skal man selv medbringe en konverter.</p>
    <p>Opløsning: op til 4K. Output signal fra den medbragte computer bliver scaleret ned til 900x540pixels via vores AV-RACK.</p>
    <p>Audio input: 3,5 line-in jackstik.</p>
    <p>Mikrofoner: 1 stk. håndholdt mikrofon. 1 stk. trådløst headset.</p>
    <p>Der er ekstra omkostninger forbundet med opsætning af højtalere og lyd.</p>

    <br/><br/>
    <p>Er der specielle ønsker, som ikke er beskrevet her, så kontakt os.</p>
  </div>
